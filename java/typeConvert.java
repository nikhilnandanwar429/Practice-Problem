public class typeConvert {
    public static void main(String args[]){

        int num = 15;
        System.out.println(num);

        float num2 = num;
        System.out.println(num2);

        char ch = 'a';
        System.out.println(ch);

        char ch2 = 'b';
        System.out.println(ch2);

        int number = (int)ch;
        System.out.println(number);


    }
}
