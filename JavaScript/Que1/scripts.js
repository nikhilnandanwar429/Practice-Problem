// SWITCH CASE

let color = "red";

switch(color){
    case "red" :
        console.log("stop");
        break;
    
    case "yellow" :
        console.log("slow down");
        break;
    
    case "green" :
        console.log("Go");
        break;
    
    default :
        console.log("Broken Light");
}


